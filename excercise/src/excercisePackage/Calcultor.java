package excercisePackage;

public class Calcultor {
	
		
		public int addition(int a, int b)
		{
			return a + b;
		}
		
		public int substraction(int a, int b)
		{
			return a - b;
		}
		
		public int multiplication(int a, int b)
		{
			return a*b;
		}
		
		public double devision(double a, double b)
		{
			return a / b;
		}
		
		
		
		
		
	
}
